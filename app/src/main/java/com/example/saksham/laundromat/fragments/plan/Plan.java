public class Plan {
    private int id;
    private int washes;
    private int iron;
    private int price;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getWashes() {
        return washes;
    }

    public void setWashes(int washes) {
        this.washes = washes;
    }

    public int getIron() {
        return iron;
    }

    public void setIron(int iron) {
        this.iron = iron;
    }

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }
}
